void APlayerCharacter::OnMovementModeChanged(EMovementMode PrevMode, uint8 PreviousCustomMode)
{
	Super::OnMovementModeChanged(PrevMode, PreviousCustomMode);
	if (PrevMode == MOVE_Swimming)
	{
		ChangeToDefaultInputMode();
	}
	else
	{
		ChangeToSwimInputMode();
	}
}

